		<h2>Các câu hỏi thường gặp (FAQs)</h2>

		<div class="space-y-6">
			<div>
				<h4>Lương Gross là gì?</h4>
				<p>Lương Gross (hay còn gọi là lương gộp/lương trước thuế) là tổng thu nhập của người lao động, bao gồm cả thuế, các khoản đóng bảo hiểm (BHYT, BHXH, BHTN), và các phụ cấp khác. Mức lương thực nhận của bạn thường sẽ thấp hơn mức này vì phải trích ra một phần để đóng bảo hiểm và nộp thuế thu nhập cá nhân.</p>
			</div>

			<div>
				<h4>Lương Net là gì?</h4>
				<p>Lương Net (hay còn gọi là lương ròng/lương sau thuế) là số tiền người lao động thực nhận sau khi trừ các khoản thuế, bảo hiểm, giảm trừ gia cảnh, và các khoản khác (nếu có). Lương Net <strong>không bao gồm</strong> thuế thu nhập cá nhân.</p>
			</div>

			<div>
				<h4>Công thức tính lương Gross và Net</h4>
				<ul>
					<li><strong>Lương Net</strong> = Tổng thu nhập - (Thuế TNCN + BHXH + BHYT + BHTN + Các khoản khấu trừ khác)</li>
					<li><strong>Lương Gross</strong> = Lương Net + BHXH + BHYT + BHTN + Thuế TNCN + Các chi phí khác</li>
				</ul>
			</div>

			<div>
				<h4>Lương Gross hay lương Net có lợi hơn cho người lao động?</h4>
				<p>
					Thoạt nhìn, có thể bạn cho rằng lương Net có lợi hơn vì đó là số tiền bạn thực nhận. Tuy nhiên trên thực tế, dù bạn đàm phán bằng cách nào thì nhà tuyển dụng cũng sẽ tính toán để chi phí phải trả tổng cộng nằm trong một khoảng ngân sách nhất định. Về cơ bản lương công ty chi trả cho bạn vẫn không đổi.
				</p>
				<p>
					Tuy nhiên nếu tinh ý, bạn sẽ thấy rằng khi sử dụng lương Gross, mức đóng bảo hiểm và thuế của bạn sẽ minh bạch hơn. Với lương Gross, bạn sẽ chủ động tính toán được số tiền bảo hiểm công ty đóng cho mình (để hưởng quyền lợi thai sản, hưu trí, thất nghiệp sau này). Việc đàm phán bằng lương Gross luôn là lựa chọn đảm bảo quyền lợi lâu dài tốt nhất cho người lao động.
				</p>
			</div>
			
